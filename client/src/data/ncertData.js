// Helper: Get chapters for a specific grade and subject
export const getChapters = (grade, subject) => {
  const gradeNum = parseInt(grade);
  return ncertData[gradeNum]?.[subject] || [];
};

// Helper: Get a specific chapter by ID
export const getChapterById = (grade, subject, chapterId) => {
  const chapters = getChapters(grade, subject);
  return chapters.find(ch => ch.id === chapterId) || null;
};

// Helper: Get available subjects for a grade
export const getNCERTSubjects = (grade) => {
  const gradeNum = parseInt(grade);
  return ncertData[gradeNum] ? Object.keys(ncertData[gradeNum]) : [];
};

// Helper: Check if NCERT data is available for grade+subject
export const hasNCERTData = (grade, subject) => {
  const gradeNum = parseInt(grade);
  return !!(ncertData[gradeNum]?.[subject]?.length);
};

// Helper: Get next/prev chapter
export const getAdjacentChapters = (grade, subject, chapterId) => {
  const chapters = getChapters(grade, subject);
  const currentIndex = chapters.findIndex(ch => ch.id === chapterId);
  return {
    prev: currentIndex > 0 ? chapters[currentIndex - 1] : null,
    next: currentIndex < chapters.length - 1 ? chapters[currentIndex + 1] : null,
    current: chapters[currentIndex] || null,
    total: chapters.length,
    currentNumber: currentIndex + 1,
  };
};

export default ncertData;
